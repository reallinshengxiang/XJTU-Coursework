[X,Y,Z]=sphere(500);
m=30;
z=1.2*(-2*m:2*m)/m;
r=ones(size(z));
theta=(0:m)/m*2*pi;
x1=r'*cos(theta);
y1=r'*sin(theta);
z1=z'*ones(1,1+m);
z2=r'*cos(theta);
y2=r'*sin(theta);
x2=z'*ones(1,1+m);
x3=r'*cos(theta);
z3=r'*sin(theta);
y3=z'*ones(1,1+m);
aa=find((2*X).^2+(2*Y).^2<1);
bb=find((2*X).^2+(2*Z).^2<1);
cc=find((2*Z).^2+(2*Y).^2<1);
Z(aa)=NaN;
Y(bb)=NaN;
X(cc)=NaN;
dd=find(x1.^2+y1.^2+z1.^2<4);
ee=find(x2.^2+y2.^2+z2.^2<4);
ff=find(x3.^2+y3.^2+z3.^2<4);
z2(ee)=NaN;
z1(dd)=NaN;
z3(ff)=NaN;
mesh(x1,y1,z1);
hold on;
mesh(x2,y2,z2);
hold on;
mesh(x3,y3,z3);
mesh(2*X,2*Y,2*Z);
axis equal;
axis off;